    <h1>Introduccion</h1>
    <p>PHP es el lenguaje de lado servidor más extendido en la web. Nacido en 1994, 
        se trata de un lenguaje de creación relativamente reciente, aunque con la 
        rapidez con la que evoluciona Internet parezca que ha existido toda la vida. 
        Es un lenguaje que ha tenido una gran aceptación en la comunidad de desarrolladores, 
        debido a la potencia y simplicidad que lo caracterizan, así como al soporte 
        generalizado en la mayoría de los servidores de hosting, hasta los más simples y económicos.</p>
        <p>La facilidad de PHP se basa en que permite embeber pequeños fragmentos de 
            código dentro de lo que sería una página común creada con HTML. 
            Esos scripts PHP nos permiten realizar determinadas acciones de una 
            forma fácil y eficaz, pudiendo realizar todo tipo de tareas, de las más simples 
            a las más complejas. Esta combinación de PHP dentro del marco de un documento HTML 
            es lo que permite a desarrolladores sin prácticamente nada de experiencia crear
             comportamientos atractivos de una manera sencilla, una de las claves del éxito 
             del lenguaje. En resumen, con PHP escribimos scripts dentro del código HTML. 
             Como ya estamos familiarizados con HTML, empezar a desarrollar con PHP es 
              inmediato. Por otra parte, y es aquí donde reside su mayor interés, PHP
               ofrece un sinfín de funciones para la explotación de todo tipo de recursos,
                entre los que destacan las bases de datos, a las que podremos acceder de 
                una manera llana, sin complicaciones.</p>
