    <h1>Mezclar HTML/Array o Arreglos</h1>
    <p>Un array en PHP es en realidad un mapa ordenado. Un mapa es un
         tipo de datos que asocia valores con claves. Este tipo se
         optimiza para varios usos diferentes; se puede emplear como un array,
          lista (vector), tabla asociativa (tabla hash - una implementación de 
          un mapa), diccionario, colección, pila, cola, y posiblemente más. 
          Ya que los valores de un array pueden ser otros arrays, también 
          son posibles árboles y arrays multidimensionales.</p>
    <p>Una explicación sobre tales estructuras de datos está fuera del alcance de este manual, aunque se proporciona al menos un ejemplo de cada uno de ellos. Para más información, consulte la extensa literatura que existe sobre este amplio tema.</p>
<pre>
array(
    clave  => valor,
    clave2 => valor2,
    clave3 => valor3,
    ...
    )
</pre>
