    <h1>Estructuras condicionales</h1>
    <p>Las estructuras condicionales son utilizadas 
        para tomar decisiones en función de que se cumpla o no una determinada condición.</p>
    <p>La estructura condicional es:</p>
    <pre>
    if(condición){
        instrucciones;
    }
    </pre>
    <p>Por ejemplo:</p>
    <pre>
    $num = 7;
    if($num > 5){
        echo “El número es mayor que 5”;
    }       
    </pre>
